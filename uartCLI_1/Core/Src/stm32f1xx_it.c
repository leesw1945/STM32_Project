void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)  // USART1에서 수신한 경우
    {
        if (rxData != '\n' && rxData != '\r') {
            cmdBuffer[idx++] = rxData;  // 줄바꿈이 아니면 버퍼에 저장
        } else {
            cmdBuffer[idx] = '\0';      // 명령어 종료 (문자열 끝)
            processCommand(cmdBuffer);  // 명령어 처리
            idx = 0;                    // 버퍼 인덱스 초기화
        }

        HAL_UART_Receive_IT(&huart1, &rxData, 1);  // 다음 수신 대기
    }
}


