void processCommand(char* cmd)  // 문자열의 시작주소를 받아서 처리하겠다.
{
    if (strcmp(cmd, "LED ON") == 0) {  // 문자열이 cmd와 LED ON이 같으면  0을 리턴
        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);  // c13 LOW LED를 킨다.
        ledStatus = 1;
        HAL_UART_Transmit(&huart1, (uint8_t*)"LED turned ON\r\n", 15, 100);
    }
    else if (strcmp(cmd, "LED OFF") == 0) {
        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
        ledStatus = 0;
        HAL_UART_Transmit(&huart1, (uint8_t*)"LED turned OFF\r\n", 16, 100);
    }
    else if (strcmp(cmd, "STATUS") == 0) {
        if (ledStatus)
            HAL_UART_Transmit(&huart1, (uint8_t*)"LED is ON\r\n", 11, 100);
        else
            HAL_UART_Transmit(&huart1, (uint8_t*)"LED is OFF\r\n", 12, 100);
    }
    else {
        HAL_UART_Transmit(&huart1, (uint8_t*)"Unknown command\r\n", 17, 100);
    }
}
